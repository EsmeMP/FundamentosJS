// Declaracion de variables con let y const
// var -- en desuso


// Variables let; Cambian de valores
// declaracion
let age;
console.log(age);
// inicializacion
age = 23;
console.log(age);


// declaracion e inicializacion
let name = "Francisco";
console.log(name);
name = "Paco";
console.log(name);


// constantes const; no cambairan de valor
// Siempre deben de ir inicializadas
const PI = 3.1416;
// PI = 3.15;
console.log(PI);

